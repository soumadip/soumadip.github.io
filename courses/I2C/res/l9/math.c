#include <stdio.h>
#include <math.h>

int main()
{
    printf("%f\n",sqrt(10.0));
    printf("%f\n",exp(4.0));
    printf("%f\n",log(4.0));
    printf("%f\n",log10(100.0));
    printf("%f\n",fabs(-5.2));
    printf("%f\n",ceil(4.5));
    printf("%f\n",floor(-4.5));
    printf("%f\n",pow(4.0,.5));
    printf("%f\n",fmod(4.5,2.0));
    printf("%f\n",sin(0.0));
    printf("%f\n",cos(0.0));
    printf("%f\n",tan(0.0));
    return 0;
}
